#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*str;

	str = (unsigned char *)s;
	while (n--)
	{
		str++;
		if (*str == c)
		{
			return (str);
		}
	}
	return ('\0');
}
