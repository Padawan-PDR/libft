#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	*nb;
	int		i;

	nb = ft_itoa(n);
	i = 0;
	while (nb[i])
	{
		write(fd, &nb[i], 1);
		i++;
	}
	free(nb);
}
